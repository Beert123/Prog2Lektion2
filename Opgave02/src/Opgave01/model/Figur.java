package Opgave01.model;

public class Figur {

    private double xpos;
    private double ypos;

    public Figur(double xpos, double ypos) {
        this.xpos = xpos;
        this.ypos = ypos;
    }


    public double getXpos() {
        return xpos;
    }

    public double getYpos() {
        return ypos;
    }
}
